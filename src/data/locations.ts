// Helper functions to get location data
export const getLocationsByState = (): Record<string, string[]> => {
  const locationsByState: Record<string, string[]> = {};
  locations.forEach(location => {
    if (!locationsByState[location.state]) {
      locationsByState[location.state] = [];
    }
    locationsByState[location.state].push(location.city);
  });
  // Sort states alphabetically
  return Object.keys(locationsByState).sort().reduce((obj: Record<string, string[]>, key) => {
    obj[key] = locationsByState[key].sort();
    return obj;
  }, {});
};
export const getAllStates = (): string[] => {
  return [...new Set(locations.map(location => location.state))].sort();
};
export const getAllCities = (): string[] => {
  return [...new Set(locations.map(location => `${location.city}, ${location.state}`))].sort();
};
export const getCitiesByState = (state: string): string[] => {
  return locations.filter(location => location.state === state).map(location => location.city).sort();
};
export const getFormattedLocations = (): string[] => {
  return locations.map(location => location.state === 'Remote' ? 'Remote' : `${location.city}, ${location.state}`).sort();
};